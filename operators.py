# Arithmetic operators

x = 40
y  = 32
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus :It returns the remainder after division.

# Comparison operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) # Equal to
print(x != y) # Not equal to

#Assignment Operators
a = 10
print(a)

a += 2 #Operator comes first then addition
print(a)

# Logical Operators: and, or, not
print(34 < 50 and 56 > 200) #True is given out if all conditions are true. if any is not true a false is given out.
print(34 < 50 or 56 > 200) #If any condition is true a True is given out.
print(not(34 < 50 or 56 > 200)) #Reverses the outcome.