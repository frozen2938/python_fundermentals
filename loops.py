# While Loop

number = 5 # initialize a number
while number <= 10: #condition
    print(number)
    number += 1 #incrementing

# Decrement
num = 30
while num >= 25:
    print(num)
    num -= 1

# For Loop
for x in range(105,110):
    print("Number is", x)

devices = ["laptop", "Mouse", "Computer"]
for y in devices:
    print(y)